/* ES6를 사용해 함수를 작성합니다. ------------------------------------------------------ */

var fibonacci = function (n) {
  // 함수 코드 로직을 작성합니다.
  return n;
};

var numberWithComma = function (n) {
  // 함수 코드 로직을 작성합니다.
  return n.toString();
};

/* 테스트 코드를 작성합니다. ----------------------------------------------------------- */

// fibonacci 유틸리티
// fibonacci(6)    →  8
// fibonacci(12)   →  144
// fibonacci(18)   →  2584

// numberWithComma 유틸리티
// numberWithComma(900);      → '900'
// numberWithComma(9900);     → '9,900'
// numberWithComma(1398700);  → '1,398,700'
